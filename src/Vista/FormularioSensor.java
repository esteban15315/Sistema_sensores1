    private void btnAgregarActualizacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActualizacionActionPerformed
        try {
        int numSensor = Integer.parseInt(txtNumSensor.getText());
        String descripcion = txtDescripcion.getText();
        String estado = comboEstado.getSelectedItem().toString();
        
        Actualizar_sistema_de_sensores.agregarActualizacion(numSensor, descripcion, estado);
        Actualizar_sistema_de_sensores.cargarActualizaciones((DefaultTableModel)tablaActualizaciones.getModel());
        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Número de sensor inválido", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnAgregarActualizacionActionPerformed

