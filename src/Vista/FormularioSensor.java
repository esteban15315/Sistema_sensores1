    private void btnActualizarEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarEstadoActionPerformed
        int filaSeleccionada = tablaActualizaciones.getSelectedRow();
    if (filaSeleccionada >= 0) {
        int numSensor = (int)tablaActualizaciones.getValueAt(filaSeleccionada, 0);
        String nuevoEstado = comboEstado.getSelectedItem().toString();
        
        Actualizar_sistema_de_sensores.actualizarEstadoSensor(numSensor, nuevoEstado);
        Actualizar_sistema_de_sensores.cargarActualizaciones((DefaultTableModel)tablaActualizaciones.getModel());
    } else {
        JOptionPane.showMessageDialog(this, "Seleccione un sensor de la tabla", 
                                   "Advertencia", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_btnActualizarEstadoActionPerformed

